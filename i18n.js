// 当前语言
let currentLang = 'en';

// 获取翻译文本
function t(key) {
    return translations[currentLang][key] || key;
}

// 切换语言
function switchLanguage(lang) {
    if (translations[lang]) {
        currentLang = lang;
        updatePageText();
        
        // 如果输入框的内容是默认文本，则也更新它
        const textInput = document.getElementById('textInput');
        const defaultTextZh = translations['zh'].inputPlaceholder;
        const defaultTextEn = translations['en'].inputPlaceholder;
        
        if (textInput.value === defaultTextZh || textInput.value === defaultTextEn) {
            textInput.value = translations[lang].inputPlaceholder;
            // 重新生成预览图
            if (window.generateImage) {
                generateImage(false);
            }
        }

        // 更新选择框的值
        const langSelect = document.getElementById('langSelect');
        if (langSelect) {
            langSelect.value = lang;
        }
    }
}

// 初始化语言选择器
document.addEventListener('DOMContentLoaded', function() {
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        // 设置初始值
        langSelect.value = currentLang;
        
        // 添加change事件监听器
        langSelect.addEventListener('change', function(e) {
            switchLanguage(e.target.value);
        });
    }
    
    // 初始化页面文本
    updatePageText();
});

// 更新页面文本
function updatePageText() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const attrName = element.getAttribute('data-i18n-attr');
        
        if (key) {
            if (attrName) {
                // 如果指定了属性名，则翻译该属性（比如 placeholder）
                element.setAttribute(attrName, t(key));
            } else {
                // 否则翻译文本内容
                element.textContent = t(key);
            }
        }
    });

    // 特别处理输入框的 placeholder
    const textInput = document.getElementById('textInput');
    if (textInput) {
        textInput.placeholder = t('inputPlaceholder');
    }
} 